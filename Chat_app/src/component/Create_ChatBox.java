    private String encodeImageToBase64(File imageFile) throws IOException {
        try (FileInputStream fileInputStream = new FileInputStream(imageFile)) {
            byte[] imageBytes = new byte[(int) imageFile.length()];
            fileInputStream.read(imageBytes);
            return Base64.getEncoder().encodeToString(imageBytes);
        }
    }

    private ImageIcon decodeBase64ToImage(String base64String) throws IOException {
        byte[] imageBytes = Base64.getDecoder().decode(base64String);
        ByteArrayInputStream bis = new ByteArrayInputStream(imageBytes);
        Image image = ImageIO.read(bis);
        return new ImageIcon(image);
    }

    private void filterUserList(String query) {
        menuLisCbo.removeAll();
        query = query.toLowerCase();
        if (query.isEmpty()) {
            for (Model_User_Account acc : userAccount) {
                menuLisCbo.add(new Item_People_Cbo(acc), "wrap");
            }
        } else {
            for (Model_User_Account acc : userAccount) {
                if (acc.getUserName().toLowerCase().contains(query)) {
                    menuLisCbo.add(new Item_People_Cbo(acc), "wrap");
                }
            }
        }
        menuLisCbo.revalidate();
        menuLisCbo.repaint();
    }

