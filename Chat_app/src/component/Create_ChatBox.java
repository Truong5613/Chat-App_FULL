    private void cmdGanAnhActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdGanAnhActionPerformed
        // TODO add your handling code here:
        JnaFileChooser fileChooser = new JnaFileChooser();
        fileChooser.addFilter("Image Files", "jpg", "png", "jpeg");
        boolean result = fileChooser.showOpenDialog(this);
        if (result) {
            File selectedFile = fileChooser.getSelectedFile();
            try {
                image = encodeImageToBase64(selectedFile);
            } catch (IOException ex) {
                Logger.getLogger(Create_ChatBox.class.getName()).log(Level.SEVERE, null, ex);
            }
            String filePath = selectedFile.getAbsolutePath();
            imageAvatar1.setImage(new ImageIcon(filePath));
            repaint();
            revalidate();
        } else {
            System.out.println("Người dùng đã hủy chọn ảnh.");
        }
    }//GEN-LAST:event_cmdGanAnhActionPerformed

